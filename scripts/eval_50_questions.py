def shorten(s, n=56):
    s = s.replace("\n", " ").strip()
    return s if len(s) <= n else s[:n - 1] + "…"


def main():
    embedder = E5Embedder(settings.embedding_model_name, device=settings.device)
    retriever = SimpleRetriever(embedder=embedder, index_version=settings.index_version)

    SEP = "=" * 128
    HDR = f"{'#':<3} {'Кат.':<9} {'Score':<7} {'Вопрос':<52} Топ-1 / FAQ-ответ"
    print(SEP)
    print(HDR)
    print(SEP)

    stats = {"ok": 0, "mid": 0, "bad": 0, "faq": 0}

    with SessionLocal() as session:
        for i, (cat, q, is_faq) in enumerate(QUESTIONS, 1):
            if is_faq:
                ans = FAQ_ANSWERS.get(q, "—")
                print(f"{i:<3} {cat:<9} {'FAQ':<7} {shorten(q, 52):<52} ✦ {shorten(ans, 58)}")
                stats["faq"] += 1
                continue

            results = retriever.search(session, q, k=1)
            if not results:
                print(f"{i:<3} {cat:<9} {'—':<7} {shorten(q, 52):<52} — нет результатов —")
                stats["bad"] += 1
                continue

            r = results[0]
            if r.score >= 0.85:
                mark, key = "✓", "ok"
            elif r.score >= 0.80:
                mark, key = "~", "mid"
            else:
                mark, key = "✗", "bad"
            stats[key] += 1

            snippet = shorten(r.chunk_text.replace("\n", " "), 58)
            print(f"{i:<3} {cat:<9} {r.score:.3f}  {shorten(q, 52):<52} {mark} {snippet}")

    print(SEP)
    rag_total = stats["ok"] + stats["mid"] + stats["bad"]
    pct_ok = stats["ok"] / rag_total * 100 if rag_total else 0
    pct_mid = stats["mid"] / rag_total * 100 if rag_total else 0
    pct_bad = stats["bad"] / rag_total * 100 if rag_total else 0
    print(f"\nИтог (RAG, {rag_total} вопросов):")
    print(f"  ✓ ≥0.85  : {stats['ok']:>2} ({pct_ok:.0f}%)")
    print(f"  ~ 0.80–0.85: {stats['mid']:>2} ({pct_mid:.0f}%)")
    print(f"  ✗ <0.80  : {stats['bad']:>2} ({pct_bad:.0f}%)")
    print(f"  ✦ FAQ    : {stats['faq']:>2} (из справочника)")
    print()
    print("Легенда: ✓ хорошо  ~ приемлемо  ✗ плохо  ✦ ответ из справочника компании")


if __name__ == "__main__":
    main()
