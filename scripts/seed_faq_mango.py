def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--dry-run", action="store_true")
    ap.add_argument("--no-embed", action="store_true",
                    help="skip embedding computation (faster, but legacy cosine matcher will ignore those entries)")
    args = ap.parse_args()

    embed_once = None
    if not args.no_embed:
        try:
            from src.rag.embedder import E5Embedder
            from src.core.config import settings
            log.info("Initialising E5 embedder (10–30s on first run)…")
            embed_once = E5Embedder(settings.embedding_model_name, device=settings.device)
            log.info("Embedder ready")
        except Exception as e:
            log.warning("Embedder unavailable — entries will be stored without embedding: %s", e)

    inserted, skipped = 0, 0
    with SessionLocal() as s:
        for entry in SEED:
            q = entry["question"].strip()
            a = entry["answer"].strip()
            existing = s.execute(select(FaqEntry).where(FaqEntry.question == q)).scalar_one_or_none()
            if existing is not None:
                skipped += 1
                log.info("SKIP (exists): %s", q[:70])
                continue

            emb_json = None
            if embed_once is not None:
                try:
                    vec = embed_once.embed_queries([q])[0]
                    emb_json = json.dumps(vec.tolist())
                except Exception as e:
                    log.warning("embed failed for %r: %s", q[:50], e)

            row = FaqEntry(question=q, answer=a, embedding=emb_json, active=True)
            if not args.dry_run:
                s.add(row)
            inserted += 1
            log.info("ADD: %s", q[:70])

        if args.dry_run:
            log.info("DRY RUN — no commit. Would insert %d / skip %d", inserted, skipped)
        else:
            s.commit()
            log.info("Done — inserted %d, skipped %d", inserted, skipped)


if __name__ == "__main__":
    main()
