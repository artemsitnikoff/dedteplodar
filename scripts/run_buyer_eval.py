def shorten(s, n=52):
    return s if len(s) <= n else s[:n-1] + "…"


def main():
    from src.core.config import settings
    embedder = E5Embedder(settings.embedding_model_name, device=settings.device)
    retriever = HybridRetriever(embedder=embedder, index_version=settings.index_version)

    COMPANY_Q = {q for _, q in QUESTIONS if _ == "Компания"}

    header = f"{'#':<3} {'Кат.':<10} {'Score':<7} {'Вопрос':<54} Топ-1 результат / FAQ-ответ"
    sep = "=" * 130
    print(sep)
    print(header)
    print(sep)

    with SessionLocal() as session:
        for i, (cat, q) in enumerate(QUESTIONS, 1):
            if q in FAQ_ANSWERS:
                faq = FAQ_ANSWERS[q]
                print(f"{i:<3} {cat:<10} {'FAQ':<7} {shorten(q, 54):<54} ✦ {shorten(faq, 60)}")
                continue

            results = retriever.search(session, q, k=1)
            if not results:
                print(f"{i:<3} {cat:<10} {'—':<7} {shorten(q, 54):<54} — нет результатов —")
                continue

            r = results[0]
            score = f"{r.score:.3f}"
            mark = "✓" if r.score >= 0.85 else ("~" if r.score >= 0.80 else "✗")
            snippet = shorten(r.chunk_text.replace("\n", " "), 55)
            print(f"{i:<3} {cat:<10} {score:<7} {shorten(q, 54):<54} {mark} {snippet}")

    print(sep)
    print()
    print("Легенда: ✓ score≥0.85 (хорошо)  ~ 0.80–0.85 (приемлемо)  ✗ <0.80 (плохо)  ✦ FAQ-ответ из справочника")


if __name__ == "__main__":
    main()
